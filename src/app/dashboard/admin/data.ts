export function getChecklistScore(checklist?: DevChecklist): { done: number; total: number; pct: number } {
  if (!checklist) return { done: 0, total: 5, pct: 0 };
  const done = checklist.items.filter(i => i.done).length;
  return { done, total: 5, pct: Math.round((done / 5) * 100) };
}

export function getOverallChecklistScoreFromEntries(entries: ChangelogEntry[]): { done: number; total: number; pct: number } {
  let done = 0;
  let total = 0;
  for (const entry of entries) {
    const cl = CHANGELOG_CHECKLISTS[entry.id];
    const score = getChecklistScore(cl);
    done += score.done;
    total += score.total;
  }
  return { done, total, pct: total > 0 ? Math.round((done / total) * 100) : 0 };
}

